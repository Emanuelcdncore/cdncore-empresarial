// Build anchor: each lang block ends with "cart_item": "..." followed by closing brace of the export
// Find each lang's cart_item line and insert after it

// Order: process in reverse to preserve indices
const allLangs = [...Object.keys(keys), ...fallbackLangs];

// Find cart_item anchor per language using the export name as context
for (const lang of allLangs) {
  const exportName = langExports[lang];
  const langKeys = keys[lang] || keys['en']; // fallback to English text

  // Find the export block start
  const exportStart = src.indexOf(`export const ${exportName}`);
  if (exportStart === -1) { console.log('Export not found:', exportName); continue; }

  // Find cart_item within this block
  const searchFrom = exportStart;
  const cartItemIdx = src.indexOf('"cart_item":', searchFrom);
  if (cartItemIdx === -1) { console.log('cart_item not found for', lang); continue; }

  const lineEnd = src.indexOf('\n', cartItemIdx);
  const insertBlock = '\n' + Object.entries(langKeys).map(([k, v]) => `  "${k}": "${v.replace(/"/g, '\\"')}",`).join('\n');
  src = src.slice(0, lineEnd) + insertBlock + src.slice(lineEnd);
  console.log('Injected legal keys for', lang);
}

